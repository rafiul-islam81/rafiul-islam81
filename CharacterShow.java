public class CharacterShow {
    public static void main(String[] args) {
        for(char c='a'; c<='z'; c++) {
            System.out.println(c);
        }
        System.out.println("============================================");
        System.out.println("============================================");
        for(char c='A'; c<='Z'; c++) {
            System.out.println(c);
        }
    }
}
